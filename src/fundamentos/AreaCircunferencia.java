package fundamentos;

public class AreaCircunferencia {
	
	public static void main(String[] args) {
		
		double raio = 3.4;
		final double PI = 3.14159; // colocando inicio transforma a variavel em uma constante (não muda o valor)
		
		double area = PI * raio * raio; // calculo do raio da transferiencia
		
		System.out.println(area);
	}

}
